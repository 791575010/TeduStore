package cn.tedu.store.service.ex;

public class TestFormatException extends ServiceException{

	private static final long serialVersionUID = -67623871277455412L;

	public TestFormatException() {
		super();
	}

	public TestFormatException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TestFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public TestFormatException(String message) {
		super(message);
	}

	public TestFormatException(Throwable cause) {
		super(cause);
	}
	
	
}
